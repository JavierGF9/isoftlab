package org.dominio;

import java.util.Date;

public class Factura {
	String titulo;
	Date fecha;
	String artista;
	double precio;
	public String getTitulo() {
		return titulo;
	}
	public Date getFecha() {
		return fecha;
	}
	public String getArtista() {
		return artista;
	}
	public double getPrecio() {
		return precio;
	}
	
	
}
